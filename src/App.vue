<template>
	<Navbar :user="user" />
	<RouterView />
</template>

<script>
import Navbar from "./components/Navbar.vue"

import { useRouter } from "vue-router"

export default {
	components: {
		Navbar
	},
	setup() {
		console.log("Welcome to FakeTwitter!")
		document.title = "🐦️ FakeTwitter"

		const user = {
			loggedIn: false
		}

		const router = useRouter()

		if (!user.loggedIn)
			router.push({ name: "Login" })

		return {
			user
		}
	}
}

</script>

